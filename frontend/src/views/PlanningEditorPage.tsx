function formatMs(ms: number): string {
  if (ms < 1000) return `${ms}ms`;
  if (ms < 60_000) return `${(ms / 1000).toFixed(1)}s`;
  const m = Math.floor(ms / 60_000);
  const s = Math.floor((ms % 60_000) / 1000);
  return `${m}m ${s}s`;
}

function buildSolverErrorOutput(planning: Planning | null): string | null {
  if (!planning || planning.status !== 'error' || !planning.lastErrorMessage) {
    return null;
  }

  const lines = [
    `ERREUR: ${planning.lastErrorMessage}`,
    ...(planning.errorDetails ?? []),
    planning.errorHint ? `Piste: ${planning.errorHint}` : null,
  ].filter((line): line is string => Boolean(line));

  return lines.join('\n');
}

function getConsoleOutputFromPlanning(planning: Planning | null): string | null {
  const errorOutput = buildSolverErrorOutput(planning);
  if (errorOutput) {
    return errorOutput;
  }
  return planning?.solutionOutput ?? null;
}

function buildFeasibleSolutionsConsoleOutput(
  solutions: Array<{ output: string }>,
  finalOutput?: string
): string {
  const history = solutions
    .map((solution, index) => `=== Solution faisable #${index + 1} ===\n${solution.output}`)
    .join('\n\n');

  if (finalOutput && finalOutput.trim().length > 0) {
    if (!history) {
      return `=== Résultat final (dernière solution faisable) ===\n${finalOutput}`;
    }
    return [
      '=== Résultat final (dernière solution faisable) ===',
      finalOutput,
      '',
      '=== Historique des solutions faisables trouvées ===',
      history,
    ].join('\n');
  }

  return history;
}

